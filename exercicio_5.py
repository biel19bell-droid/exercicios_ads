# Cálculo de Área: Peça a largura e a altura de uma parede em metros e calcule a sua área

base = float(input("Digite a medida em metros da base da parede: "))
altura = float(input("Digite a medida em metros da altura da parede: ")) 
area = base * altura 

print(f"A área da parede é de {area} m²") 